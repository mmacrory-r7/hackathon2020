    <h1> Blind XSS Page </h1>
    <p>
    Blind XSS is a special type of stored XSS in which the data retrieval point is not
    accessible by the attacker - for example, due to lack of privileges. This makes the 
    vulnerability very difficult to test for using conventional techniques. In many cases,
    there is no hint whatsoever in the application's visible functionality that a vulnerability exists.
